/* 6. Write a program to display character and string literals along with their ASCII values.*/


class Que6 {
public static void main (String[] args) {
	char ch1 = 'A';
	char ch2 = 'Z';
	String str = "Hello, World!";
	System.out.println("Character Literal 1: " + ch1 + " ASCII " + (int) ch1);
	System.out.println("Character Literal 2: " + ch2 + " ASCII " + (int) ch2);
	System.out.println("String Literal : " + str );
	}
}