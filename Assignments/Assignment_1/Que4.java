/* 4. Write a program to calculate the average of three int numbers using typecasting to display the result in double.  */

class Que4 {
	public static void main (String[] args) {
	int a = 10 ;
	int b = 20 ;
	int c = 30 ;
	int d = (a+b+c)/3;
	double D = d;
	System.out.println("Average of given 3 numbers is: " +d);
	System.out.println("Average of given 3 numbers in double is: " +D);
	}
}