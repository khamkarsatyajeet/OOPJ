/* 10. Write a program to compare two integers using all relational operators (==, !=, >, <, >=, <=) and display the results. */


class Que9 {
 public static void main (String[] args) {
	int a = 10;
	int b = 20;
	System.out.println("a == b: " + (a==b));	
	System.out.println("a != b: " + (a!=b));
	System.out.println("a > b: " + (a>b));
	System.out.println("a < b: " + (a<b));
	System.out.println("a >= b: " + (a>=b));
	System.out.println("a <= b: " + (a<=b));	
}
}