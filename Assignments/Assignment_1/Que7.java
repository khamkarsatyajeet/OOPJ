/* 7. Write a program that uses boolean literals to control program flow in an if-else statement. */

class Que7 {
public static void main (String[] args) {
	boolean a = true;
	Boolean b = false;
	if (a) {
	System.out.println("I am an Indian !") ;
	}
	else {
	System.out.println("I am not an Indian !");
	}
	if (b) {
	System.out.println("I am a Maharashtrian !") ;
	}
	else {
	System.out.println("I am not a Maharashtrian !") ;
	}
	}
}
